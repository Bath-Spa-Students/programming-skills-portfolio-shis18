#Think of your favorite mode of transportation, such as a motorcycle or a car, and make a list that stores several examples. Use your list

#to print a series of statements about these items, such as “I would like to own a Honda motorcycle.”


# List of modes of transportation
modes_of_transportation = ["motorcycle", "car", "bicycle", "scooter", "train"]

# Generating statements
for mode in modes_of_transportation:
    print(f"I would like to own a {mode.capitalize()}.")

# Output:
# I would like to own a Motorcycle.
# I would like to own a Car.
# I would like to own a Bicycle.
# I would like to own a Scooter.
# I would like to own a Train.
